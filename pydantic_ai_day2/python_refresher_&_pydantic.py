def announce(func):
    """Announces when a function starts and finishes."""

    @functools.wraps(func)
    def wrapper(*args, **kwargs):
        print(f"Starting {func.__name__}...")
        result = func(*args, **kwargs)
        print(f"Finished {func.__name__}.")
        return result

    return wrapper


@announce
def greet(name):
    return f"Hello, {name}!"


