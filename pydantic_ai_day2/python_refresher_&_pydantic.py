def create_user(name, age):
    """
    Creates a user, attempting to convert age to an integer if it's a string.
    Raises TypeError if name is not a string or if age cannot be converted to an integer.
    """
    if not isinstance(name, str):
        raise TypeError("name must be a string")

    # Attempt to convert age to an integer if it's a string
    if isinstance(age, str):
        try:
            age = int(age)
        except ValueError:
            raise TypeError("age must be an integer or a string that can be converted to an integer")
    elif not isinstance(age, int):
        # If it's not a string and not an int, it's an invalid type
        raise TypeError("age must be an integer")

    print(f"{name} is {age} years old.")

